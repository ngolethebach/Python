# Nursery Rhymes


user_number = int(input("Enter a number between 1 and 7: "))
if user_number == 1:
    print("One for sorrow")
elif user_number == 2:
    print("Two for joy")
elif user_number == 3:
    print("Three for a girl")
elif user_number == 4:
    print("Four for a boy")
elif user_number == 5:
    print("Five for silver")
elif user_number == 6:
    print("Six for gold")
elif user_number == 7:
    print("Seven for a secret never to be told")      
else:
    print("Not a permitted number")
    